#include <stdio.h>

double inch2cm(double cm) 
{
	return cm / 2.54;
}

double m2ft(double m) 
{
	return m * 3.2808;
}

double m2yard(double m) 
{
	return m * 1.0936;
}

double m2mile(double m) 
{
	return m * 0.00062137;
}

double gram2oz(double gram)
{
	return gram * 0.035274;
}

double gram2pound(double gram) 
{
	return gram * 0.0022046;
}

double pyung2sqmeter(double pyung) 
{
	return pyung * 3.3058;
}

double pyung2ha(double pyung)
{
	return pyung * 0.000330579;
}

double liter2gal(double liter) 
{
	return liter * 0.264172;
}

double liter2doe(double liter) 
{
	return liter * 0.554352;
}
